
public class TestaCaracteres {
	
	public static void main(String[] args) {
		char letra = 'a';
		System.out.println("O caracter é a letra: " + letra);
		
		char valor = 66;
		// na tabela de caracteres, a letra A corresponde ao numero 65, e assim segue.
		System.out.println(valor);
		
		valor = (char)(valor + 1);
		System.out.println(valor);
		
		String palavra = "Alura cursos online de tecnologia em ";
		System.out.println(palavra);
		
		palavra = palavra + 2020;
		System.out.println(palavra);
		
	}

}
